package com.example.demo;

public class Laptop {

	private int lid;
	private String lmaker;
	private int ram;
	
	public int getLid() {
		return lid;
	}
	public void setLid(int lid) {
		this.lid = lid;
	}
	public String getLmaker() {
		return lmaker;
	}
	public void setLmaker(String lmaker) {
		this.lmaker = lmaker;
	}
	public int getRam() {
		return ram;
	}
	public void setRam(int ram) {
		this.ram = ram;
	}
	
}
